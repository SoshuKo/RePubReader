  function changeMap(imageSrc, btnElement) {
    const resolved = imageSrc.includes('/') ? imageSrc : `assets/maps/${imageSrc}`;
    document.getElementById('map-image').src = resolved;
    document.querySelectorAll('.map-btn').forEach(btn => btn.classList.remove('active'));
    btnElement.classList.add('active');
  }

  window.onload = function() {
    const colorCodeImg = document.getElementById('color-code-img');
    if (colorCodeImg.complete) {
      extractPoints(colorCodeImg);
    } else {
      colorCodeImg.onload = () => extractPoints(colorCodeImg);
    }
  };

  function extractPoints(img) {
    const canvas = document.createElement('canvas');
    canvas.width = img.width;
    canvas.height = img.height;
    const ctx = canvas.getContext('2d');
    ctx.drawImage(img, 0, 0);

    let imageData;
    try {
      imageData = ctx.getImageData(0, 0, canvas.width, canvas.height);
    } catch (e) {
      console.error("ピクセルデータの取得に失敗しました。", e);
      return;
    }

    const data = imageData.data;
    const pointsLayer = document.getElementById('points-layer');

    for (let i = 0; i < data.length; i += 4) {
      const r = data[i], g = data[i + 1], b = data[i + 2], a = data[i + 3];
      if (a === 0) continue;

      if (r === 0 && g === 0) {
        const hexString = b.toString(16);
        if (/^[0-9]+$/.test(hexString)) {
          const parishId = parseInt(hexString, 10);
          if (parishId >= 1 && parishId <= 79) {
            const pixelIndex = i / 4;
            const x = pixelIndex % canvas.width;
            const y = Math.floor(pixelIndex / canvas.width);
            createPoint(x, y, canvas.width, canvas.height, parishId, pointsLayer);
          }
        }
      }
    }
  }

  function createPoint(x, y, mapWidth, mapHeight, parishId, layer) {
    const point = document.createElement('div');
    point.className = 'map-point';
    point.style.left = (x / mapWidth * 100) + '%';
    point.style.top = (y / mapHeight * 100) + '%';
    point.title = '教区 ' + parishId;

    point.onclick = function() {
      openPanel(parishId);
    };

    layer.appendChild(point);
  }

  function openPanel(parishId) {
    const data = parishDatabase[parishId] || {
      name: `第${parishId}教区`,
      archdiocese: "未設定",
      state: "未設定",
      population: "未調査"
    };

    document.getElementById('p-title').innerText = data.name;
    document.getElementById('p-archdiocese').innerText = data.archdiocese;
    document.getElementById('p-state').innerText = data.state;
    document.getElementById('p-population').innerText = data.population;

    const linkBtn = document.getElementById('p-link');
    if (data.pageUrl) {
      linkBtn.href = data.pageUrl;
      linkBtn.style.display = 'block';
    } else {
      linkBtn.style.display = 'none';
    }

    document.getElementById('info-panel').classList.add('open');
    document.getElementById('overlay').classList.add('show');
  }

  function closePanel() {
    document.getElementById('info-panel').classList.remove('open');
    document.getElementById('overlay').classList.remove('show');
  }
